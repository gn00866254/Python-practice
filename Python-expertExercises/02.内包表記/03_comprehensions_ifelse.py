birthday = ["19930911", "20010621", "19801110", "19980507", 
            "20100101", "20090519", "20140712", "20110323",
            "19990415", "20001224"]

#年を切り取り、2000年生まれ以降はリストに追加。
print([ birth[:4] for birth in birthday if int(birth[:4])>=2000 ])
